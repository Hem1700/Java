import java.lang.*;

public class main {

    public static void main (String args []){

        // Practicing Switch Case

        int n = 6;
        switch(n){
            case 1:
                System.out.println("One");
                break;
            case 2:
                System.out.println("Two");
                break;
            case 3:
                System.out.println("Three");
                break;
            default:
                System.out.println("Invalid Input");
            }

    }
    
}
